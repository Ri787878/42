#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	t;
	char	*str;

	str = (char *)s;
	t = 0;
	while (t < n)
	{
		str[t] = c;
		t++;
	}
	return (s);
}
