#include <stdio.h>
#include <string.h>

void	ft_bzero(void *s, size_t n)
{
	size_t	t;
	char	*str;

	str = (char *)s;
	t = 0;
	while (t < n)
	{
		str[t] = '0';
		t++;
	}
	return ;
}
