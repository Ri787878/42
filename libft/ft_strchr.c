#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	t;

	t = 0;
	while (s[t] != '\0')
	{
		if (s[t] == c)
			return ((char *)&s[t]);
		t++;
	}
	if (c == '\0')
		return ((char *)&s[t]);
	return (NULL);
}
