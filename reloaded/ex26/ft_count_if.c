int	ft_count_if(char **tab, int (*f)(char *))
{
	int	n;
	int	sum;

	sum = 0;
	n = 0;
	while (tab[n] != 0)
	{
		if ((*f)(tab[n]) == 1)
			sum++;
		n++;
	}
	return (sum);
}
